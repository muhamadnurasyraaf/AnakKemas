    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {

        $data = $request->validate([
            'title' => 'required',
            'content' => 'required',
            'student_id' => 'required'
        ]);
        
        $data['reportedBy'] = Auth::user()->id;

        Report::create($data);

        return redirect()->route('dashboard.teacher');
    }

